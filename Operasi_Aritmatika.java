public class Operasi_Aritmatika {
    public static void main(String[] args) {
        int a, b, c, d, e, f, g;
        a = 12;
        b = 6;
        c = a + b; //Penjumlahan
        d = a - b; //Pengurangan
        e = a * b; //Perkalian
        f = a / b; //Pembagian
        g = a % b; //Modulus

        System.out.println("Hasil dari 12 + 6 adalah : " + c);
        System.out.println("Hasil dari 12 - 6 adalah : " + d);
        System.out.println("Hasil dari 12 x 6 adalah : " + e);
        System.out.println("Hasil dari 12 / 6 adalah : " + f);
        System.out.println("Hasil modulus dari 12 dan 6 adalah : " + g);
    }
}
