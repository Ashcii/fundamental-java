//Operator yang membuat angka menjadi bertambah 1 atau berkurang 1 angka
public class Increment_Decrement {
    public static void main(String[] args) {
        int a, b;
        a = 10;
        b = 5;
        
        System.out.println("Hasil dari a++ adalah: " + a++);
        System.out.println("Hasil dari ++a adalah: " + ++a);
        System.out.println("Hasil dari b-- adalah: " + b--);
        System.out.println("Hasil dari --b adalah: " + --b);
    }
}